const month = 'Fab';

switch (month) {
    case 'Jan':
        console.log('Jan');
        break;
    case 'Fab':
        console.log('Fab');
        break;
    case 'March':
        console.log('March');
        break;
    case 'April':
        console.log('April');
        break;
    default:
        console.log('default case match');
        break;
}